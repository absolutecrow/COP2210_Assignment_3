    public void keyPressed(KeyEvent k)
    {
        if (k.getKeyCode() == KeyEvent.VK_UP)
        {
            controls.move_up();
        }
        else if (k.getKeyCode() == KeyEvent.VK_LEFT)
        {   
            controls.move_left();            
        }
        else if (k.getKeyCode() == KeyEvent.VK_DOWN)
        {
            controls.move_down();
        }
        else if (k.getKeyCode() == KeyEvent.VK_RIGHT)
        {
            controls.move_right();
        }
    }
    
