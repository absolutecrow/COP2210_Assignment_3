    private void startGameActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_startGameActionPerformed
    {//GEN-HEADEREND:event_startGameActionPerformed
        if (playerNameField.getText() != null && !(playerNameField.getText().equals("")))
        {
            controls.setPlayerName(playerNameField.getText());
            diaryLabel.setText(playerNameField.getText() + "'s Diary");
            diaryText.setText("Hello, my name is " + playerNameField.getText() + ". I shall be exploring this house in search of spookiness to squelch.");
            this.setVisible(true);
            tutorialDialog.setVisible(false);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Name cannot be empty.");
        }
    }//GEN-LAST:event_startGameActionPerformed

